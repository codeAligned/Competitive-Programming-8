int ab(int n){
	return n < 0 ? -n : n;
}
int main(){
	//freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
	int N, A, B, C;
	readInt(N);
	while(N--){
		readInt(A);
		readInt(B);
		readInt(C);
		bool completed = false;
		for(int x = -(int)sqrt(C); x*x <= C && !completed; x++)
			for(int y = -(int)sqrt(C-(x*x)); (x*x+y*y) <= C && !completed; y++){
				if(!x || !y || x == y || y == (A-x-y) || x == (A-x-y)) continue;
				if((A*A-C) == (2*x*y + 2*y*(A-x-y) + 2*x*(A-x-y)) && x*y*(A-x-y) == B){
					printf("%d %d %d\n", x, y, (A-x-y));
					completed = true;
				}
			}
		if(!completed)
			printf("No solution.\n");
	}
	return 0;
}