void readIntArr(int *n, int len){
	for(int i = 0; i < len; i++)
		if(!(~readInt(n[i]))){
			n[i] = 0;
			return;
		}
}
void readLLArr(long long *n, int len){
	for(int i = 0; i < len; i++)
		if(!(~readLL(n[i]))){
			n[i] = 0;
			return;
		}
}
void readDoubleArr(double *n, int len){
	for(int i = 0; i < len; i++)
		if(!(~readDouble(n[i]))){
			n[i] = 0;
			return;
		}
}

int main(){
	freopen("rockers.in", "r", stdin);
	freopen("rockers.out", "w", stdout);
	int N, M, T;
	readInt(N);
	readInt(T);
	readInt(M);
	int dp[M+5][T+5], weight;
	memset(dp, 0, sizeof(dp));
	for(int k = 1; k <= N; k++){
		readInt(weight);
		for(int i = M; i >= 1; i--)
			for(int j = T; j >= weight; j--){
				int pos = max(dp[i][j-weight], dp[i-1][T]) + 1;
				if(dp[i][j] < pos)
					dp[i][j] = pos;
			}
	}
	printf("%d\n", dp[M][T]);
	return 0;
}