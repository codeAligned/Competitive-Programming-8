int seqlen[5000005];
long long ans[5000005];
int dfs(long long cur){
	if(seqlen[cur]) return seqlen[cur];
	if(cur%2){
		int need = 1;
		long long temp = cur;
		temp = temp*3+1;
		while(temp > 5000000){
			need++;
			if(temp%2)
				temp = temp*3+1;
			else
				temp /= 2;
		}
		return (seqlen[cur] = dfs(temp)+need);
	}else
		return (seqlen[cur] = dfs(cur/2)+1);
}
