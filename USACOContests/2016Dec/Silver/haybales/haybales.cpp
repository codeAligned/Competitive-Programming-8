int binSearch(int lo, int hi, int *arr, int k){
	if(lo == hi)
		return lo;
	int mid = (lo + hi)/2;
	if(arr[mid] < k)
		return binSearch(mid+1, hi, arr, k);
	else
		return binSearch(lo, mid, arr, k);
}
